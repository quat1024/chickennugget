package quaternary.chickennugget.tconstruct;

import com.google.common.collect.ImmutableList;

import mezz.jei.api.ingredients.IIngredients;
import net.minecraft.item.ItemStack;
import net.minecraftforge.fluids.FluidStack;
import slimeknights.tconstruct.plugin.jei.JEIPlugin;
import slimeknights.tconstruct.plugin.jei.casting.CastingRecipeWrapper;

public class RecipeWrapperCastChicken extends CastingRecipeWrapper {
	public RecipeWrapperCastChicken() {
		super(ChickenCastingRecipe.getDummyCastingRecipe(), JEIPlugin.castingCategory.castingBasin);
	}
	
	@Override
	public boolean isValid(boolean checkCast) {
		return true;
	}

	@Override
	public void getIngredients(IIngredients ingredients) {
		ingredients.setInputs(ItemStack.class, cast);
		ingredients.setInputs(FluidStack.class, inputFluid);
		ingredients.setOutputs(ItemStack.class, ImmutableList.of());
	}
}
